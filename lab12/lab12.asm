; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character

;R0:Character that will be printed
;R1:Not used
;R2:Chararacter location
;R3:row counter
;R4:Font location
;R5:DATA
;R6:Shift counter, Column counter

.ORIG x3000
	LEA	R4,FONT_DATA	;load the location of the chart
	LDI	R2,Char		;load the character to be printed
	AND	R3,R3,#0	;seet R3 to zero
	ADD	R6,R3,#4
	ADD	R3,R3,#15	;Set up the row counter

L_Shift	ADD	R2,R2,R2	;Using left shift to get the location to start
	ADD	R6,R6,#-1
	BRp	L_Shift
	
	ADD	R4,R2,R4	;Set the location to start
Row	LDR	R5,R4,#0	;load the value in chart to R5
	AND	R6,R6,#0	;Set column counter to zero
	ADD	R6,R6,#8

Column	LDI	R0,O_ZERO	;load the character that is for zero
	ADD	R5,R5,#0	;set cc
	BRzp	DIS_OUT		;check the weather the digit is zero or one
	LDI	R0,O_ONE	;load the character that is for one
DIS_OUT	OUT

	ADD	R5,R5,R5	;left shift R5
	ADD	R6,R6,#-1	;decrease column counter
	BRp	Column

	LD	R0,ASCIINL	;load next line to R0
	OUT			;change to next line
	ADD	R4,R4,#1	;add the location
	ADD	R3,R3,#-1	;decrease row counter
	BRzp	Row

	HALT

O_ZERO	.FILL	x5000
O_ONE	.FILL	x5001
Char	.FILL	x5002

