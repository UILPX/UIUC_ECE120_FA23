; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.

;R0:Character that will be printed
;R1:Memory location of the current chracter
;R2:Start location of the funt of the current character.
;R3:row counter
;R4:Starting location of the font
;R5:Current value (0 or 1) of the font
;R6:Shift counter and column counter

.ORIG x3000
	AND	R3,R3,#0	;seet R3 to zero
	ADD	R3,R3,#15	;Set up the row counter
	AND	R1,R1,#0	;seet R1 to zero
	AND	R2,R2,#0	;seet R2 to zero
	AND	R5,R5,#0	;seet R5 to zero
	LEA	R4,FONT_DATA	;load the starting location of the font

Row	LD	R1,STRI		;load the character to be printed
	LDR	R2,R1,#0

NewC	AND	R6,R6,#0
	ADD	R6,R6,#4	;set the left shift counter to calculate the staring location
L_Shift	ADD	R2,R2,R2	;Using left shift to get the location to start
	ADD	R6,R6,#-1	;decrease the shift counter
	BRp	L_Shift
	
	ADD	R2,R2,R4	;Set the starting location of the current chracter
	LDR	R5,R2,#0	;load the value in chart to R5

	AND	R6,R6,#0
	ADD	R6,R6,#8	;set the collum counter
Column	LDI	R0,ZERO		;load the character that is for zero
	ADD	R5,R5,#0	;set cc
	BRzp	DIS_OUT		;check the weather the digit is zero or one
	LDI	R0,ONE		;load the character that is for one
DIS_OUT	OUT

	ADD	R5,R5,R5	;left shift R5
	ADD	R6,R6,#-1	;decrease the column counter
	BRp	Column

	ADD	R1,R1,#1	;go the the next character to be printed
	LDR	R2,R1,#0	;load the ASCII value of the next character to R2
	BRnp	NewC

	LD	R0,ASCIINL	;load next line to R0
	OUT			;change to next line
	ADD	R4,R4,#1	;go to the font data for the next row
	ADD	R3,R3,#-1	;decrease row counter
	BRzp	Row

	HALT

ZERO	.FILL	x5000		;the memory location of the character for 0
ONE	.FILL	x5001		;the memory location of the character for 1
STRI	.FILL	x5002		;starting location of the string to be printed

ASCIINL	.FILL	xA		;ASCII command for the next line

